import turtle

myturtle = turtle.Turtle('turtle')

myturtle.pen()
myturtle.pencolor('red')
myturtle.pensize(5)
myturtle.goto(50, 70)

myturtle.forward(100)
myturtle.left(90)
myturtle.forward(200)
myturtle.left(90)
myturtle.forward(100)
myturtle.left(90)
myturtle.forward(200)
myturtle.left(90)

turtle.done()